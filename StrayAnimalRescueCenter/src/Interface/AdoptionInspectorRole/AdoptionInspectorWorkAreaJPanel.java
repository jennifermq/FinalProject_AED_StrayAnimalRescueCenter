    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblApplication.getSelectedRow();

        if (selectedRow < 0){
            return;
        }

        AdoptionRecord ar = (AdoptionRecord)tblApplication.getValueAt(selectedRow, 0);
        UserAccount ua = userAccount;
        ar.setInspector(ua.getEmployee());
        ar.setStatus(AdoptionRecord.PROCESSING_STATUS);
        
        populateTable();
        JOptionPane.showMessageDialog(null, "Work has been assigned to you. Please view it in My Inspections panel.");
    }//GEN-LAST:event_btnAssignActionPerformed

