    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel)tblApplication.getModel();
        model.setRowCount(0);
        
        for(AdoptionRecord ar: organization.getAdoptionRecordDir().getAdoptionRecordList()) {
            if(ar.getStatus() == AdoptionRecord.SUBMITTED_STATUS) {
                Object[] row = new Object[5];
                row[0] = ar;
                row[1] = ar.getAnimal().getAnimalId();
                row[2] = ar.getAdoptor().getFirstName() + " " + ar.getAdoptor().getLastName();
                row[3] = ar.getAdoptor().getPhoneNum();
                row[4] = ar.getAdoptor().getEmail();
                model.addRow(row);
            }
        }
    }
    
