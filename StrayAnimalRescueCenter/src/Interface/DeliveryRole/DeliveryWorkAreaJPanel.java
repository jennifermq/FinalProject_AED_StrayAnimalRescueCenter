    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDelivery.getSelectedRow();

        if (selectedRow < 0){
            return;
        }

        DeliveryHistory dh = (DeliveryHistory)tblDelivery.getValueAt(selectedRow, 0);
        UserAccount ua = userAccount;
        dh.setCourier(ua.getEmployee());
        dh.setStatus(DeliveryHistory.ASSIGNED_STATUS);

        populateTable();

        JOptionPane.showMessageDialog(null, "Work has been assigned to you. Please view it in My Deliveries panel.");
    }//GEN-LAST:event_btnAssignActionPerformed

