    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel)tblDelivery.getModel();
        model.setRowCount(0);
        
        for(DeliveryHistory dh: enterprise.getDeliveryHistoryDir().getDeliveryHistoryList()) {
            if(dh.getStatus().equals(DeliveryHistory.WAITING_STATUS)) {
                Object[] row = new Object[4];
                row[0] = dh;
                row[1] = dh.getAnimal().getType().getValue();
                row[3] = dh.getAdopter().getAddress().getCity();
                row[3] = dh.getAdopter().getFirstName() + " " + dh.getAdopter().getLastName();
                model.addRow(row);
            }
        }
    }
    
